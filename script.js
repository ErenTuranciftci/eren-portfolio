function getSavedLanguage() {
  try {
    return localStorage.getItem('portfolio-language');
  } catch (error) {
    return null;
  }
}

function saveLanguage(lang) {
  try {
    localStorage.setItem('portfolio-language', lang);
  } catch (error) {
    // localStorage kapalıysa dil seçimi yalnızca açık sekmede geçerli kalır.
  }
}

function initializePortfolio() {
  const menuButton = document.querySelector('.menu-button');
  const navLinks = document.querySelector('.nav-links');
  const header = document.querySelector('.site-header');
  const langButtons = document.querySelectorAll('.lang-button');
  const translatable = document.querySelectorAll('[data-i18n]');
  const translatableAria = document.querySelectorAll('[data-i18n-aria]');
  const yearElement = document.getElementById('year');
  const backToTopButton = document.getElementById('back-to-top');

  // Footer, sayfanın diğer JavaScript özelliklerinden bağımsız çalışır.
  if (yearElement) {
    yearElement.textContent = String(new Date().getFullYear());
  }

  if (backToTopButton) {
    backToTopButton.addEventListener('click', (event) => {
      event.preventDefault();
      window.scrollTo({
        top: 0,
        behavior: window.matchMedia('(prefers-reduced-motion: reduce)').matches
          ? 'auto'
          : 'smooth'
      });

      // Adres çubuğunda gereksiz #top kalmasını önler.
      if (window.history && window.history.replaceState) {
        window.history.replaceState(null, '', `${window.location.pathname}${window.location.search}`);
      }
    });
  }

  function applyLanguage(lang) {
    const safeLang = translations[lang] ? lang : 'tr';
    const dictionary = translations[safeLang];

    document.documentElement.lang = safeLang;
    document.title = dictionary.pageTitle;

    const description = document.querySelector('meta[name="description"]');
    if (description) description.setAttribute('content', dictionary.pageDescription);

    translatable.forEach((element) => {
      const key = element.dataset.i18n;
      if (dictionary[key] !== undefined) element.textContent = dictionary[key];
    });

    translatableAria.forEach((element) => {
      const key = element.dataset.i18nAria;
      if (dictionary[key] !== undefined) element.setAttribute('aria-label', dictionary[key]);
    });

    langButtons.forEach((button) => {
      const isActive = button.dataset.lang === safeLang;
      button.classList.toggle('active', isActive);
      button.setAttribute('aria-pressed', String(isActive));
    });

    saveLanguage(safeLang);
  }

  langButtons.forEach((button) => {
    button.addEventListener('click', () => applyLanguage(button.dataset.lang));
  });

  if (menuButton && navLinks) {
    menuButton.addEventListener('click', () => {
      const isOpen = navLinks.classList.toggle('open');
      menuButton.classList.toggle('open', isOpen);
      menuButton.setAttribute('aria-expanded', String(isOpen));
      const currentLang = document.documentElement.lang || 'tr';
      menuButton.setAttribute(
        'aria-label',
        translations[currentLang][isOpen ? 'menuClose' : 'menuOpen']
      );
    });

    navLinks.querySelectorAll('a').forEach((link) => {
      link.addEventListener('click', () => {
        navLinks.classList.remove('open');
        menuButton.classList.remove('open');
        menuButton.setAttribute('aria-expanded', 'false');
      });
    });
  }

  if (header) {
    const updateHeader = () => header.classList.toggle('scrolled', window.scrollY > 10);
    window.addEventListener('scroll', updateHeader, { passive: true });
    updateHeader();
  }

  const revealElements = document.querySelectorAll('.reveal');
  if ('IntersectionObserver' in window) {
    const observer = new IntersectionObserver((entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add('visible');
          observer.unobserve(entry.target);
        }
      });
    }, { threshold: 0.12 });

    revealElements.forEach((element) => observer.observe(element));

    const sectionObserver = new IntersectionObserver((entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting || !navLinks) return;
        navLinks.querySelectorAll('a').forEach((link) => {
          link.classList.toggle('active', link.getAttribute('href') === `#${entry.target.id}`);
        });
      });
    }, { rootMargin: '-35% 0px -55%', threshold: 0 });

    document.querySelectorAll('main section[id]').forEach((section) => sectionObserver.observe(section));
  } else {
    revealElements.forEach((element) => element.classList.add('visible'));
  }

  const savedLanguage = getSavedLanguage();
  const browserLanguage = navigator.language.toLowerCase().startsWith('tr') ? 'tr' : 'en';
  applyLanguage(savedLanguage || browserLanguage);
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initializePortfolio, { once: true });
} else {
  initializePortfolio();
}
